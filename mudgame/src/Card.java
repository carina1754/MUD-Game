
public class Card {//카드 class
	private String pattern;//문양 변수
	private String number;//숫자 변수
	
	public String getPattern() {//문양 getter
		return pattern;
	}
	public void setPattern(String pattern) {//문양 setter
		this.pattern = pattern;
	}
	public String getNumber() {//숫자 getter
		return number;
	}
	public void setNumber(String number) {//숫자 setter
		this.number = number;
	}
}
